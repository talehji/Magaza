package Object;

/**
 *
 * @author Pallas
 */
public class Members {

    public int idMember;
    public String Ad;
    public String isActive;
    public String Password;
    public String Soyad;
    public String Telefon;

    public Members() {
    }

    public Members(int idMember, String Ad, String isActive, String Password, String Soyad, String Telefon) {
        this.idMember = idMember;
        this.Ad = Ad;
        this.isActive = isActive;
        this.Password = Password;
        this.Soyad = Soyad;
        this.Telefon = Telefon;
    }

}
